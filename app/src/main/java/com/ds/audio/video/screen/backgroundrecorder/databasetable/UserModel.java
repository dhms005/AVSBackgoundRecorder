package com.ds.audio.video.screen.backgroundrecorder.databasetable;

public class UserModel {

    String ID, v_time, v_duration, v_cam;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getV_time() {
        return v_time;
    }

    public void setV_time(String v_time) {
        this.v_time = v_time;
    }

    public String getV_duration() {
        return v_duration;
    }

    public void setV_duration(String v_duration) {
        this.v_duration = v_duration;
    }

    public String getV_cam() {
        return v_cam;
    }

    public void setV_cam(String v_cam) {
        this.v_cam = v_cam;
    }
}